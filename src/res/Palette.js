/**
 * @typedef {import('react').CSSProperties} CSSProperties
 */

/**
 * Set a container to take 100% of its parent's width.
 * @type {CSSProperties}
 */
export const width100Percent = {
  width: '100%',
}

/**
 * Enable flex context for a container.
 * @type {CSSProperties}
 */
export const flex = {
  display: 'flex',
}

/**
 * Sets a flex container's layout to column.
 * @type {CSSProperties}
 */
export const flexColumn = {
  flexDirection: 'column',
}
